package poly.dto;

public class EatDTO {
	private String eatSeqNo;
	private String title;
	private String eatContents;
	private String regDt;
	private String chgDt;
	private String userId;
	private String userName;
	
	public String getEatSeqNo() {
		return eatSeqNo;
	}
	public void setEatSeqNo(String eatSeqNo) {
		this.eatSeqNo = eatSeqNo;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getEatContents() {
		return eatContents;
	}
	public void setEatContents(String eatContents) {
		this.eatContents = eatContents;
	}
	public String getRegDt() {
		return regDt;
	}
	public void setRegDt(String regDt) {
		this.regDt = regDt;
	}
	public String getChgDt() {
		return chgDt;
	}
	public void setChgDt(String chgDt) {
		this.chgDt = chgDt;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	
	
	
}
