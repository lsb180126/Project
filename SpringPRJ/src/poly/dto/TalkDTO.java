package poly.dto;

public class TalkDTO {
	private String talkSeqNo;
	private String title;
	private String talkContents;
	private String fileName;
	private String filePath;
	private String userName;
	private String regUserSeqNo;
	private String regDt;
	private String chgUsrSeqNo;
	private String chgDt;
	
	public String getTalkSeqNo() {
		return talkSeqNo;
	}
	public void setTalkSeqNo(String talkSeqNo) {
		this.talkSeqNo = talkSeqNo;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getTalkContents() {
		return talkContents;
	}
	public void setTalkContents(String talkContents) {
		this.talkContents = talkContents;
	}
	public String getFileName() {
		return fileName;
	}
	public void setFileName(String fileName) {
		this.fileName = fileName;
	}
	public String getFilePath() {
		return filePath;
	}
	public void setFilePath(String filePath) {
		this.filePath = filePath;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getRegUserSeqNo() {
		return regUserSeqNo;
	}
	public void setRegUserSeqNo(String regUserSeqNo) {
		this.regUserSeqNo = regUserSeqNo;
	}
	public String getRegDt() {
		return regDt;
	}
	public void setRegDt(String regDt) {
		this.regDt = regDt;
	}
	public String getChgUsrSeqNo() {
		return chgUsrSeqNo;
	}
	public void setChgUsrSeqNo(String chgUsrSeqNo) {
		this.chgUsrSeqNo = chgUsrSeqNo;
	}
	public String getChgDt() {
		return chgDt;
	}
	public void setChgDt(String chgDt) {
		this.chgDt = chgDt;
	}
	
	
	
	
	
}
