package poly.dto;

public class TalkDTO {
	private String talkSeqNo;
	private String title;
	private String talkContents;
	private String userName;
	private String regDt;
	private String chgDt;
	
	public String getTalkSeqNo() {
		return talkSeqNo;
	}
	public void setTalkSeqNo(String talkSeqNo) {
		this.talkSeqNo = talkSeqNo;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getTalkContents() {
		return talkContents;
	}
	public void setTalkContents(String talkContents) {
		this.talkContents = talkContents;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getRegDt() {
		return regDt;
	}
	public void setRegDt(String regDt) {
		this.regDt = regDt;
	}
	public String getChgDt() {
		return chgDt;
	}
	public void setChgDt(String chgDt) {
		this.chgDt = chgDt;
	}
	
	
	
	
	
	
}
