package poly.dto;

public class SellDTO {
	private String sellSeqNo;
	private String title;
	private String sellContents;
	private String userName;
	private String regDt;
	private String chgDt;
	
	public String getSellSeqNo() {
		return sellSeqNo;
	}
	public void setSellSeqNo(String sellSeqNo) {
		this.sellSeqNo = sellSeqNo;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getSellContents() {
		return sellContents;
	}
	public void setSellContents(String sellContents) {
		this.sellContents = sellContents;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getRegDt() {
		return regDt;
	}
	public void setRegDt(String regDt) {
		this.regDt = regDt;
	}
	public String getChgDt() {
		return chgDt;
	}
	public void setChgDt(String chgDt) {
		this.chgDt = chgDt;
	}
	
	
	
	
}
