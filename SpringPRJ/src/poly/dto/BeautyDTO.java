package poly.dto;

public class BeautyDTO {
	private String beautySeqNo;
	private String title;
	private String beautyContents;
	private String regDt;
	private String chgDt;
	private String userId;
	private String userName;
	
	public String getBeautySeqNo() {
		return beautySeqNo;
	}
	public void setBeautySeqNo(String beautySeqNo) {
		this.beautySeqNo = beautySeqNo;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getBeautyContents() {
		return beautyContents;
	}
	public void setBeautyContents(String beautyContents) {
		this.beautyContents = beautyContents;
	}
	public String getRegDt() {
		return regDt;
	}
	public void setRegDt(String regDt) {
		this.regDt = regDt;
	}
	public String getChgDt() {
		return chgDt;
	}
	public void setChgDt(String chgDt) {
		this.chgDt = chgDt;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	
	
	
}
