package poly.dto;

public class HomegoodsDTO {
	private String homegoodsSeqNo;
	private String title;
	private String homegoodsContents;
	private String userName;
	private String regDt;
	private String chgDt;
	
	public String getHomegoodsSeqNo() {
		return homegoodsSeqNo;
	}
	public void setHomegoodsSeqNo(String homegoodsSeqNo) {
		this.homegoodsSeqNo = homegoodsSeqNo;
	}
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getHomegoodsContents() {
		return homegoodsContents;
	}
	public void setHomegoodsContents(String homegoodsContents) {
		this.homegoodsContents = homegoodsContents;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getRegDt() {
		return regDt;
	}
	public void setRegDt(String regDt) {
		this.regDt = regDt;
	}
	public String getChgDt() {
		return chgDt;
	}
	public void setChgDt(String chgDt) {
		this.chgDt = chgDt;
	}
	
	
	
}
