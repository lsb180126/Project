package poly.dto;

public class FileDTO {
	private String fileSeq;
	private String brdSeqNo;
	private String userNo;
	private String oriName;
	private String chgName;
	private String fileSize;
	private String filePath;
	private String extension;
	private String regDt;
	private String brdKind;
	
	public String getFileSeq() {
		return fileSeq;
	}
	public void setFileSeq(String fileSeq) {
		this.fileSeq = fileSeq;
	}
	public String getBrdSeqNo() {
		return brdSeqNo;
	}
	public void setBrdSeqNo(String brdSeqNo) {
		this.brdSeqNo = brdSeqNo;
	}
	public String getUserNo() {
		return userNo;
	}
	public void setUserNo(String userNo) {
		this.userNo = userNo;
	}
	public String getOriName() {
		return oriName;
	}
	public void setOriName(String oriName) {
		this.oriName = oriName;
	}
	public String getChgName() {
		return chgName;
	}
	public void setChgName(String chgName) {
		this.chgName = chgName;
	}
	public String getFileSize() {
		return fileSize;
	}
	public void setFileSize(String fileSize) {
		this.fileSize = fileSize;
	}
	public String getFilePath() {
		return filePath;
	}
	public void setFilePath(String filePath) {
		this.filePath = filePath;
	}
	public String getExtension() {
		return extension;
	}
	public void setExtension(String extension) {
		this.extension = extension;
	}
	public String getRegDt() {
		return regDt;
	}
	public void setRegDt(String regDt) {
		this.regDt = regDt;
	}
	public String getBrdKind() {
		return brdKind;
	}
	public void setBrdKind(String brdKind) {
		this.brdKind = brdKind;
	}
	
	
	
}
